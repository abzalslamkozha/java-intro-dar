public class ReplaceCharacters {
    public static void main(String[] args) {
        String originalString = "Hello, world! This is a sample text.";
        String modifiedString = originalString.replace('a', 'b');
        System.out.println("Исходная строка: " + originalString);
        System.out.println("Модифицированная строка: " + modifiedString);
    }
}
